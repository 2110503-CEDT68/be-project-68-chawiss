const Booking = require("../models/Booking");
const Dentist = require("../models/Dentist");

// @desc    Get all bookings
// @route   GET /api/v1/bookings
// @access  Private (Registered User & Admin)
exports.getBookings = async (req, res, next) => {
  let query;

  // 1. Requirement: User ดูได้เฉพาะของตัวเอง, Admin ดูได้ทั้งหมด (ข้อ 4 & 7)
  if (req.user.role !== "admin") {
    query = Booking.find({ user: req.user.id }).populate({
      path: "dentist",
      select: "name experienceYears expertise",
    });
  } else {
    // Admin ดูตาม dentistId หรือดูทั้งหมด
    if (req.params.dentistId) {
      query = Booking.find({ dentist: req.params.dentistId }).populate({
        path: "dentist",
        select: "name experienceYears expertise",
      });
    } else {
      query = Booking.find().populate({
        path: "dentist",
        select: "name experienceYears expertise",
      });
    }
  }

  try {
    const bookings = await query;
    res.status(200).json({
      success: true,
      count: bookings.length,
      data: bookings,
    });
  } catch (error) {
    console.log(error);
    return res
      .status(500)
      .json({ success: false, message: "Cannot find Booking" });
  }
};

// @desc    Get single booking
// @route   GET /api/v1/bookings/:id
// @access  Private
exports.getBooking = async (req, res, next) => {
  try {
    const booking = await Booking.findById(req.params.id).populate({
      path: "dentist",
      select: "name experienceYears expertise",
    });

    if (!booking) {
      return res
        .status(404)
        .json({
          success: false,
          message: `No booking with the id of ${req.params.id}`,
        });
    }

    // เช็คสิทธิ์: ถ้าไม่ใช่เจ้าของและไม่ใช่ admin ห้ามดู (ข้อ 4)
    if (booking.user.toString() !== req.user.id && req.user.role !== "admin") {
      return res
        .status(401)
        .json({
          success: false,
          message: "Not authorized to view this booking",
        });
    }

    res.status(200).json({ success: true, data: booking });
  } catch (error) {
    console.log(error);
    return res
      .status(500)
      .json({ success: false, message: "Cannot find Booking" });
  }
};

// @desc    Add booking
// @route   POST /api/v1/dentists/:dentistId/bookings
// @access  Private (Registered User)
exports.addBooking = async (req, res, next) => {
  try {
    req.body.dentist = req.params.dentistId;
    req.body.user = req.user.id;

    const dentist = await Dentist.findById(req.params.dentistId);
    if (!dentist) {
      return res
        .status(404)
        .json({
          success: false,
          message: `No dentist with the id of ${req.params.dentistId}`,
        });
    }

    // 2. Requirement: User จองได้เพียง "ONE session" เท่านั้น (ข้อ 3)
    const existedBooking = await Booking.findOne({ user: req.user.id });

    if (existedBooking && req.user.role !== "admin") {
      return res.status(400).json({
        success: false,
        message: `The user with ID ${req.user.id} has already made a booking. Only 1 booking is allowed.`,
      });
    }

    const booking = await Booking.create(req.body);
    res.status(201).json({ success: true, data: booking });
  } catch (error) {
    console.log(error);
    return res
      .status(500)
      .json({ success: false, message: "Cannot create Booking" });
  }
};

// @desc    Update booking
// @route   PUT /api/v1/bookings/:id
// @access  Private (Owner & Admin)
exports.updateBooking = async (req, res, next) => {
  try {
    let booking = await Booking.findById(req.params.id);

    if (!booking) {
      return res
        .status(404)
        .json({
          success: false,
          message: `No booking with the id of ${req.params.id}`,
        });
    }

    // 3. Requirement: เช็คสิทธิ์ความเป็นเจ้าของ หรือ Admin (ข้อ 5 & 8)
    if (booking.user.toString() !== req.user.id && req.user.role !== "admin") {
      return res.status(401).json({
        success: false,
        message: `User ${req.user.id} is not authorized to update this booking`,
      });
    }

    //ลบ userid ใน body ก่อน update
    const updateData = { ...req.body };
    delete updateData.user;

    booking = await Booking.findByIdAndUpdate(req.params.id, updateData, {
      new: true,
      runValidators: true,
    });

    res.status(200).json({ success: true, data: booking });
  } catch (error) {
    console.log(error);
    return res
      .status(500)
      .json({ success: false, message: "Cannot update Booking" });
  }
};

// @desc    Delete booking
// @route   DELETE /api/v1/bookings/:id
// @access  Private (Owner & Admin)
exports.deleteBooking = async (req, res, next) => {
  try {
    const booking = await Booking.findById(req.params.id);

    if (!booking) {
      return res
        .status(404)
        .json({
          success: false,
          message: `No booking with the id of ${req.params.id}`,
        });
    }

    // 4. Requirement: เช็คสิทธิ์ความเป็นเจ้าของ หรือ Admin (ข้อ 6 & 9)
    if (booking.user.toString() !== req.user.id && req.user.role !== "admin") {
      return res.status(401).json({
        success: false,
        message: `User ${req.user.id} is not authorized to delete this booking`,
      });
    }

    await booking.deleteOne();
    res.status(200).json({ success: true, data: {} });
  } catch (error) {
    console.log(error);
    return res
      .status(500)
      .json({ success: false, message: "Cannot delete Booking" });
  }
};
